// Initialize campaign page
document.addEventListener('DOMContentLoaded', function() {
    const campaignId = sessionStorage.getItem('selectedCampaignId') || '3';
    loadCampaignDetail(campaignId);
    setupBackButton();
    setupContributeButton();
});

// Load campaign detail
function loadCampaignDetail(campaignId) {
    const campaign = campaignDetails[campaignId];
    if (!campaign) {
        console.error('Campaign not found');
        return;
    }

    // Update hero section
    document.getElementById('campaign-hero').src = campaign.image;
    document.getElementById('campaign-category').textContent = campaign.category;
    document.getElementById('campaign-title').textContent = campaign.title;
    document.getElementById('campaign-location').textContent = campaign.location;

    // Update stats
    document.getElementById('campaign-goal').textContent = campaign.goal;
    document.getElementById('campaign-raised').textContent = campaign.raised;
    document.getElementById('campaign-percentage').textContent = campaign.percentage + '%';
    document.getElementById('progress-bar').style.width = campaign.percentage + '%';
    document.getElementById('campaign-backers').textContent = campaign.backers;
    document.getElementById('campaign-days').textContent = campaign.daysLeft;
    document.getElementById('campaign-impact').textContent = campaign.impact;

    // Update story
    const storyContainer = document.getElementById('campaign-story');
    storyContainer.innerHTML = campaign.story.map(paragraph => 
        `<p>${paragraph}</p>`
    ).join('');

    // Load timeline
    loadTimeline(campaign.timeline);

    // Load testimonials
    loadTestimonials(campaign.testimonials);
}

// Load timeline
function loadTimeline(timeline) {
    const timelineContainer = document.getElementById('trust-timeline');
    
    timeline.forEach((item, index) => {
        const timelineItem = document.createElement('div');
        timelineItem.className = 'relative pl-10' + (index < timeline.length - 1 ? ' pb-10' : '');
        
        const iconHtml = item.completed 
            ? `<span class="material-symbols-outlined text-on-primary text-xs" style="font-variation-settings: 'FILL' 1;">check</span>`
            : `<div class="w-2 h-2 rounded-full bg-primary animate-pulse"></div>`;
        
        const circleHtml = item.completed
            ? 'bg-primary'
            : 'border-2 border-primary bg-surface';

        timelineItem.innerHTML = `
            <div class="absolute left-0 top-1.5 w-6 h-6 rounded-full ${circleHtml} flex items-center justify-center z-10">
                ${iconHtml}
            </div>
            <div>
                <h4 class="font-headline font-bold text-on-surface">${item.title}</h4>
                <p class="text-sm text-on-surface-variant mt-1 leading-relaxed">${item.description}</p>
                <span class="text-[10px] font-label font-medium uppercase tracking-widest ${item.completed ? 'text-primary' : 'text-on-surface-variant'} mt-2 block">${item.date}</span>
            </div>
        `;
        
        timelineContainer.appendChild(timelineItem);
    });
}

// Load testimonials
function loadTestimonials(testimonials) {
    const testimonialsContainer = document.getElementById('testimonials');
    
    testimonials.forEach(testimonial => {
        const testimonialCard = document.createElement('div');
        testimonialCard.className = 'flex-none w-72 aspect-[9/16] rounded-2xl bg-surface-container relative overflow-hidden group';
        testimonialCard.innerHTML = `
            <img alt="Testimonial" class="w-full h-full object-cover opacity-60 group-hover:scale-105 transition-transform duration-500" 
                 data-alt="${testimonial.name} testimonial" 
                 src="${testimonial.image}"/>
            <div class="absolute inset-0 flex flex-col justify-end p-4 bg-gradient-to-t from-black/80 to-transparent">
                <div class="flex items-center gap-2 mb-2">
                    <span class="material-symbols-outlined text-white text-3xl" style="font-variation-settings: 'FILL' 1;">play_circle</span>
                    <div>
                        <p class="font-headline font-bold text-white text-sm">${testimonial.name}</p>
                        <p class="text-[10px] text-white/70 uppercase tracking-tighter">${testimonial.role}</p>
                    </div>
                </div>
            </div>
        `;
        
        testimonialsContainer.appendChild(testimonialCard);
    });
}

// Setup back button
function setupBackButton() {
    const backButton = document.getElementById('back-button');
    backButton.addEventListener('click', () => {
        window.history.back();
    });
}

// Setup contribute button
function setupContributeButton() {
    const contributeButton = document.getElementById('contribute-button');
    contributeButton.addEventListener('click', () => {
        window.location.href = 'contribute.html';
    });
}
